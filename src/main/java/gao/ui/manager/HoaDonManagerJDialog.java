        @Override
        public void edit
        
            () {
        int row = tblBills.getSelectedRow();
            if (row >= 0) {
                Long id = (Long) tblBills.getValueAt(row, 0);
                HoaDon hd = dao.findById(id);
                if (hd != null) {
                    this.setForm(hd);
                    this.setEditable(true);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn hóa đơn cần sửa.");
            }
        }

