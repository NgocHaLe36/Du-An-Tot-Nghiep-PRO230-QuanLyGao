    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.selectTimeRange();
        setTitle("Quản lý Doanh Thu");
    }

    @Override
    public void selectTimeRange() {
        TimeRange range = TimeRange.today();
        switch (cboTimeRanges.getSelectedIndex()) {
            case 0 ->
                range = TimeRange.today();
            case 1 ->
                range = TimeRange.thisWeek();
            case 2 ->
                range = TimeRange.thisMonth();
            case 3 ->
                range = TimeRange.thisQuarter();
            case 4 ->
                range = TimeRange.thisYear();
        }
        txtBegin.setText(XDate.format(range.getBegin(), "MM/dd/yyyy"));
        txtEnd.setText(XDate.format(range.getEnd(), "MM/dd/yyyy"));
        this.fillRevenue();
    }

    @Override
    public void fillRevenue() {
        java.util.Date beginUtil = XDate.parse(txtBegin.getText(), "MM/dd/yyyy");
        java.util.Date endUtil = XDate.parse(txtEnd.getText(), "MM/dd/yyyy");

        if (beginUtil == null || endUtil == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng ngày (MM/dd/yyyy)", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        java.sql.Date begin = new java.sql.Date(beginUtil.getTime());
        java.sql.Date end = new java.sql.Date(endUtil.getTime());

        switch (tabs2.getSelectedIndex()) {
            case 0 ->
                this.fillRevenueByCategory(begin, end);
            case 1 ->
                this.fillRevenueByUser(begin, end);
        }

    }

    private void fillRevenueByCategory(Date begin, Date end) {
        DoanhThuDAO dao = new DoanhThuDAOimpl();
        List<DoanhThu.ByLoai> items = dao.getByCategory(begin, end);
        DefaultTableModel model = (DefaultTableModel) tblByCategory2.getModel();
        model.setRowCount(0);
        items.forEach(item -> {
            Object[] row = {
                item.getTenloai(),
                String.format("%,.0f VNĐ", item.getDoanhthu()),
                item.getSoluong(),
                String.format("%,.0f VNĐ", item.getGiabanthapnhat()),
                String.format("%,.0f VNĐ", item.getGiabancaonhat()),
                String.format("%,.0f VNĐ", item.getGiabantb())
            };
            model.addRow(row);
        });
    }

    private void fillRevenueByUser(Date begin, Date end) {
        DoanhThuDAO dao = new DoanhThuDAOimpl();
        List<DoanhThu.ByUser> items = dao.getByUser(begin, end);
        DefaultTableModel model = (DefaultTableModel) tblByUser.getModel();
        model.setRowCount(0);
        items.forEach(item -> {
            Object[] row = {
                item.getTen(),
                String.format("%,.0f VNĐ", item.getDoanhthu()),
                item.getSoluongHang(),
                XDate.format(item.getFirstTime(), "hh:mm:ss dd-MM-yyyy"),
                XDate.format(item.getLastTime(), "hh:mm:ss dd-MM-yyyy")
            };
            model.addRow(row);
        });
    }
