    @Override
    public void moveTo(int index) {
        if (index < 0) {
            this.moveLast();
        } else if (index >= tblRice.getRowCount()) {
            this.moveFirst();
        } else {
            tblRice.clearSelection();
            tblRice.setRowSelectionInterval(index, index);
            this.edit();
        }
    }
